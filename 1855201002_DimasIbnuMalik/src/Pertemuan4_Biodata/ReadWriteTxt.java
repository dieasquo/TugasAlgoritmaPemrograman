    void browseFile() {

        File fileName;
        JFileChooser chooser = new JFileChooser(".");

        int openDialog = chooser.showOpenDialog(ReadWriteTxt.this);
        if (openDialog == JFileChooser.APPROVE_OPTION) {
            fileName = chooser.getSelectedFile();
            String pathFile = fileName.getPath();

            String imgPath = images.getText();

            path.setText(pathFile);

            String line = null;
            try {
                FileReader fileReader = new FileReader(pathFile);
                BufferedReader bufReader = new BufferedReader(fileReader);

                int length = 0;
                while ((line = bufReader.readLine()) != null) {
                    if (length == 0) {
                        nama.setText(line);
                    } else if (length == 1) {
                        ttl.setText(line);
                    } else if (length == 2) {
                        alamat.setText(line);
                    } else if (length == 3) {
                        motto.setText(line);
                    } else if (length == 4) {
                        email.setText(line);
                    } else if (length == 5) {
                        wa.setText(line);
                    } else if (length == 6) {
                        fb.setText(line);
                    } else if (length == 7) {
                        twitter.setText(line);
                    } else if (length == 8) {
                        insta.setText(line);
                    } else if (length == 9) {
                        prodi.setText(line);
                    } else if (length == 10) {
                        fakultas.setText(line);
                    } else if (length == 11) {
                        univ.setText(line);
                    } else if (length == 12) {
                        nim.setText(line);
                    } else if (length == 13) {
                        kelas.setText(line);
                    } else if (length == 14) {
                        images.setText(line);
                    }
                    length++;
                }

                BufferedImage image = null;

                try {
                    image = ImageIO.read(new File(images.getText()));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Gambar Error/Tidak Ditemukan");
                }
                ImageIcon imageIcon = new ImageIcon(image);
                foto.setIcon(imageIcon);

                System.out.print(length);
                bufReader.close();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

