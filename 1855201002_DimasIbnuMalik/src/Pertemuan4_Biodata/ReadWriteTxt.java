                emailEdit = email.getText();
                waEdit = wa.getText();
                fbEdit = fb.getText();
                twtEdit = twitter.getText();
                instaEdit = insta.getText();
                imagePath = images.getText();

                bw.write(namaEdit);
                bw.newLine();
                bw.write(ttlEdit);
                bw.newLine();
                bw.write(alamatEdit);
                bw.newLine();
                bw.write(motoEdit);
                bw.newLine();

                bw.write(emailEdit);
                bw.newLine();
